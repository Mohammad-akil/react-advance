export const handleReturnPrimaryImage = (images: any, images1: any) => {
  if (images1 && images1[0] !== "null") {
    let newImages: any =
      typeof images1?.[0] === "string" ? JSON.parse(images1?.[0]) : images1;
    let startOrder = newImages?.find((it: any) => it.Order === 0) ? 0 : 1;
    let newMediaUrl = newImages?.find(
      (item: any) => item.Order === startOrder
    )?.MediaURL;
    if (newMediaUrl) {
      return newMediaUrl;
    } else {
      return newImages?.[0]?.MediaURL;
    }
    return;
  } else if (images) {
    return images[0];
  }
};

