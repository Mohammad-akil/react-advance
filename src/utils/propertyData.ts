export const queryParsing = (queryString: any) => {
  if (queryString) {
    const regex = /(\w+:\([^)]+\)|\w+:\[[^\]]+\])|(\w+:[^ ]+)/g;

    const matches: any = queryString.match(regex);

    const parsedResult: any = {};

    if (matches) {
      matches.forEach((match: any) => {
        const [key, value] = match.split(":");
        parsedResult[key] = value.trim();
      });
    }
    return parsedResult;
  } else {
    return {};
  }
};

export const generateValue = (data: any, isQuotes: any) => {
  if (data?.value?.length) {
    let newValue = data?.value?.map((it: any) => {
      if (it?.includes(" ") && isQuotes) {
        return `"${it}"`;
      } else {
        return it;
      }
    });
    return newValue?.join(" OR ");
  } else {
    return data?.value?.[0]?.includes(" ") && isQuotes
      ? `"${data?.value?.[0]}"`
      : data?.value?.[0];
  }
};
